/**
 * Returns the information on whether the module supports a feature
 *
 * See {@link plugin_supports()} for more info.
 *
 * @param string $feature FEATURE_xx constant for requested feature
 * @return mixed true if the feature is supported, null if unknown
 */
function qa_supports($feature) {
    switch($feature) {
        case FEATURE_MOD_INTRO:
            return true;
        case FEATURE_SHOW_DESCRIPTION:
            return true;
        case FEATURE_GRADE_HAS_GRADE:
            return false;
        case FEATURE_BACKUP_MOODLE2:
            return true;
        default:
            return null;
    }
}

/**
 * Saves a new instance of the qa into the database
 *
 * Given an object containing all the necessary data,
 * (defined by the form in mod_form.php) this function
 * will create a new instance and return the id number
 * of the new instance.
 *
 * @param stdClass $qa Submitted data from the form in mod_form.php
 * @param mod_qa_mod_form $mform The form instance itself (if needed)
 * @return int The id of the newly inserted qa record
 */
function qa_add_instance(stdClass $qa, mod_qa_mod_form $mform = null) {
    global $DB;

    $qa->timecreated = time();
    $qa->timemodified = time();
    $qa->id = $DB->insert_record('qa', $qa);

    return $qa->id;
}

/**
 * Updates an instance of the qa in the database
 *
 * Given an object containing all the necessary data,
 * (defined by the form in mod_form.php) this function
 * will update an existing instance with new data.
 *
 * @param stdClass $qa An object from the form in mod_form.php
 * @param mod_qa_mod_form $mform The form instance itself (if needed)
 * @return boolean Success/Fail
 */
function qa_update_instance(stdClass $qa, mod_qa_mod_form $mform = null) {
    global $DB;

    $qa->timemodified = time();
    $qa->id = $qa->instance;
    $result = $DB->update_record('qa', $qa);

    return $result;
}

/**
 * Removes an instance of the qa from the database
 *
 * Given an ID of an instance of this module,
 * this function will permanently delete the instance
 * and any data that depends on it.
 *
 * @param int $id Id of the module instance
 * @return boolean Success/Failure
 */
function qa_delete_instance($id) {
    global $DB;

    if (! $qa = $DB->get_record('qa', array('id' => $id))) {
        return false;
    }

    $DB->delete_records('qa', array('id' => $qa->id));

    return true;
}

